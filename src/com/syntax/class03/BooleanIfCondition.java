package com.syntax.class03;

public class BooleanIfCondition {

	public static void main(String[] args) {
		boolean flag = true;
		
		if (flag ==true) {
			System.out.println("Hi");
			
		}else {
			System.out.println("Bye");
		}
		System.out.println(flag);//== equal and != not equal//the exclamation mark wi;; reverse the condition/ or the opposite of the condition

		//if to reverse the condition use !=
		if (flag != true) {
			System.out.println ("Hi");
		}else {
			System.out.println("Bye");
		}
		
		
	}

}
