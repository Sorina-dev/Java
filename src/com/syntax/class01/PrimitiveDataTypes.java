package com.syntax.class01;

public class PrimitiveDataTypes {

	public static void main(String[] args) {
		
	/*
	 * 
	 */
		
       // whole number data types
		
		byte num = 10;
		short num1 = 100;
		int num2 = 1000;
		// l or L
		long num3 = 1000000000L;
		
		// Decimal Number Data Types
		// F or f is for float
		
		float num4 = 12.9F;
		double num5 = 0.888;
		
		// non-number data type
		
		char add = '+';
		char letter = 'a';
		
		// true or false (Yes or No)
		// do not put Yes or No for value
		
		boolean hungry = true;
		boolean thirsty = false;
		
		
		

	}

}
