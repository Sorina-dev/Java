package com.syntax.class07;

public class OddNumbers {
public static void main(String[] args) {
	// print from 1-50
	int x = 50;
	while (x >= 1) {
		System.out.println(x);
		x--;
	}
	System.out.println("************************************************");
	// print odd numbers from 1-20
	int y = 1;
	while(y<20) {
		System.out.println(y);
		y+=2;
	}
}
}
