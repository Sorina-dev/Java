package com.class01.DataTypes;

public class VariableValues {
	public static void main(String [] args) {
		/* Create a Java Program and name it VariableValues
		 * Create variable for all data types and assign it's value by the 3 different ways of declaration
		 * Reassign values for the variables
		 * Print out each variable on a separated line
		 */
	// single line declaration and reassign values for the variables
	byte num1 = 30;
	System.out.println(num1);
	num1 = 50;
	System.out.println(num1);
	
	short num2 = 500;
	System.out.println(num2);
	num2 = 700;
	System.out.println(num2);
	
	int num3 = 767;
	System.out.println(num3);
	num3 = 768;
	System.out.println(num3);
	
	long num4 = 78473;
	System.out.println(num4);
	num4 = 7847368L;
	System.out.println(num4);
	
	float num5 = 64.8F;
	System.out.println(num5);
	num5 = 48.477f;
	System.out.println(num5);
	
	double num6 = 695.6;
	System.out.println(num6);
	num6 = 3883.9387746637;
	System.out.println(num6);
	
	char closed = '#';
	System.out.println(closed);
	closed = 'S';
	System.out.println(closed);
	
	boolean open = false;
	System.out.println(open);
	open = true;
	System.out.println(open);

	
	//two line declaration
	byte q;
	q = 2;
	short w;
	w = 23;
	int e;
	e = 245;
	long r;
	r = 756389l;
	float t;
	t = 276.7f;
	double y; 
	y = 38575.476;
	char u;
	u = 'S';
	boolean answer;
	answer = true;
	
	System.out.println(q);
	System.out.println(w);
	System.out.println(e);
	System.out.println(r);
	System.out.println(t);
	System.out.println(y);
	System.out.println(u);
	System.out.println(answer);
	
	// multi line declaration
	byte a,s,d;
	a = 22;
	s = 23;
	d = 24;
	short f,g,h;
	f = 240;
	g = 250;
	h = 260;
	int j,k,l;
	j = 5647;
	k = 5650;
	l = 5660;
	long z,x,c;
	z = 3567L;
	x = 93847L;
	c = 938476L;
	float v,b,n;
	v = 58.65f;
	b = 59.56f;
	n = 60.25f;
	double m,q1,w1;
	m = 546.588;
	q1 = 547.658;
	w1 = 548.254;
	char sunny,rainy,cloudy;
	sunny = 'S';
	rainy = 'R';
	cloudy = 'C';
	boolean libra,cancer,capricorn,aries;
	libra = true;
	cancer = false;
	capricorn = true;
	aries = false;
	System.out.println(a);
	System.out.println(s);
	System.out.println(d);
	System.out.println(f);
	System.out.println(g);
	System.out.println(h);
	System.out.println(j);
	System.out.println(k);
	System.out.println(l);
	System.out.println(z);
	System.out.println(x);
	System.out.println(c);
	System.out.println(v);
	System.out.println(b);
	System.out.println(n);
	System.out.println(m);
	System.out.println(q1);
	System.out.println(w1);
	System.out.println(sunny);
	System.out.println(rainy);
	System.out.println(cloudy);
	System.out.println(libra);
	System.out.println(cancer);
	System.out.println(capricorn);
	System.out.println(aries);
	
	
	}		
	

}
